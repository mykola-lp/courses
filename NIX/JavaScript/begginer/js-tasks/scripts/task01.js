/* 
 * task01 - Function that creates variables using camelCase, snake_case,
 * and PascalCase, and assigns them values from 0 to 2. It then displays
 * these values using alert and console.log.
 */
function task01() {
    // Declare variables with different casing styles and assign values.
    const camelCase = 0
        , snake_case = 1
        , PascalCase = 3;
  
    alert(camelCase);
    console.log(camelCase);
  
    alert(snake_case);
    console.log(snake_case);
  
    alert(PascalCase);
    console.log(PascalCase);
  
    /*
     `alert` method is opening pop-up window with message and to continue 
             manipulate with your browser you should first close it.
  
     `console.log` method is working in specific tab - Console. To interact 
                   with that tab you must first close `alter` window.        
    */
};
